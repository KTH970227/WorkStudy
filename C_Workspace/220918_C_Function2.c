int Max(int a, int b)
{
	if (a > b)
		return a;
	else
		return b;
}

int Plus(int a, int b)
{
	int c = a + b;
	return c;
}

int Minus(int a, int b)
{
	int c = a - b;
	return c;
}

int Mutiple(int a, int b)
{
	int c = a * b;
	return c;
}

int Division(int a, int b)
{
	int c = a / b;
	return c;
}